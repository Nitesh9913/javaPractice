package Arithmetic;

public class subtractionOfValues1 {
    public static void main(String[] args) {
        System.out.println("A subtracted value is ");
        byte num1 = -50;
        short num2 = -500;
        int num3 = 500000;
        float num4 = -50.9921f;
        float subtraction = num1-num2-num3-num4;
        System.out.println(subtraction);
    }
}
